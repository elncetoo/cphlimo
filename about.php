						<p>Copenhagen Limousine Service is the oldest limousine service company in Denmark and was founded by Axel Pitzner in 1962.</p>
<p>The company, however, also comprises other operators that were found even earlier than this; Marmorbilen (founded in the beginning of the 1940’ies) and Auto Royale (founded in the 1950’ies).</p>
<p>Not least was the establishment of Copenhagen Limousine Service the result of the requests of fathers of American families. They were about to send off their offspring to visit Europe and were particularly anxious about letting their daughters’ tour around on their own. Therefore private cars with chauffeurs who could also act as chaperones were soon a reality.</p>
<p>Once the service with private cars with chauffeurs was opened up for, soon the idea of limousine service was fostered and larger and more luxurious and prestigious limousines were purchased and were on offer.</p>
<p>Today, Copenhagen Limousine Service is by far the largest provider of limousine service in Denmark and provides limousines and related services to a number of different types of clients including, embassies and ministries, multinational and national companies, rock stars and other celebrities, private persons, hotels, tour operators, incoming bureaus and many more.</p>
<p>Copenhagen Limousine Service offers all types of limousine service within Denmark and abroad. Through our representation of several international limousine service companies, including a majority of the largest limousine providers in the world, we can also assist in the provision of limousines in countries far beyond the boundaries of Denmark. More than 500 cities in 120 countries on 5 continents are on offer. These are relations that have lasted in excess of 50 years.</p>
<p>By November 1996 the ownership of Copenhagen Limousine Service was transferred when Mikkel Pitzner, then managing director of Budget Pitzner and son of Axel Pitzner, purchased the company.</p>
<p>Since then the fleet has grown with the addition of several vehicles &#8211; with multiples of same model so that a fleet of identical vehicles can be delivered for larger assignments &#8211; including luxurious vehicles such as Jaguar Sovereigns (LWB), Mercedes Benz E Avantgardes, Mercedes Benz S (LWB), Rolls Royces (LWB), Volvo V70s, VW Caravelles, Mercedes Benz Sprinter and Ford Transit minivan.</p>
<p>Also in the fleet you will find several smaller deluxe sedans and other minibusses can upon request for larger assignments be supplied. Through our sister company, The Private Car Company A/S, we can also offer a number of small to full size coaches.</p>
<p>Our commitment to inducing ever improvements in everything that we do and a continuous search of excellence, striving to supersede our customers every expectations has been a driving force in our expansion of the business, tying closer knots with the clients and the community around us.</p>
